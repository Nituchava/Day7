
//display default value of premitive datatypes

package com.Programjava;

class Datatypes {
    static int a;
    static double d;
    static float f;
    static boolean istrue;
    static String p;
    static long L;
    public static void main(String[] args)
    {
        System.out.println("int default value = "+a);
        System.out.println("double default value = "+d);
        System.out.println("float default value = "+f);
        System.out.println("boolean default value = "+ istrue);
        System.out.println("String default value = "+p);
        System.out.println("Long default value is =" +L);
    }


}
